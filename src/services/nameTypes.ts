// ============================================================
// NameMatch – Canonical Name Type System
//
// NormalizedNameRecord is the single source-of-truth type
// that bridges the UI layer (BabyName) and the algorithm
// layer (NameRecord from supabase.ts).
//
// All providers normalize to this type.
// All algorithms operate on this type.
// UI components read this type (it's a superset of BabyName).
// ============================================================

import type { BabyName, Region } from '../types';

// ─────────────────────────────────────────────────────────────
// Enums / Union types
// ─────────────────────────────────────────────────────────────

export type GenderUsage = 'predominantly_male' | 'predominantly_female' | 'unisex';

export type TrendDirection = 'rising' | 'stable' | 'falling' | 'classic' | 'unknown';

export type NameSource =
  | 'local_seed'    // src/data/names.ts static array
  | 'supabase'      // Supabase `names` table (enriched / user-curated)
  | 'ssa_us'        // TODO: US Social Security Administration open data
  | 'ons_uk'        // TODO: UK Office for National Statistics
  | 'insee_fr'      // TODO: French INSEE baby name data
  | 'behindthename' // TODO: BehindTheName public API
  | 'ai_suggested'; // Generated by AI, not verified

// ─────────────────────────────────────────────────────────────
// Core canonical type
// ─────────────────────────────────────────────────────────────

export interface NormalizedNameRecord {
  // ── Identity ──────────────────────────────────────────────
  id: string;
  displayName: string;               // "Zoë" — rendered as-is
  normalizedName: string;            // "zoe" — ASCII, lowercase, diacritics stripped

  // ── Demographics ──────────────────────────────────────────
  language: string | null;           // primary language family ("Romance", "Germanic"…)
  country: string | null;            // primary associated country (ISO-style: "France")
  genderUsage: GenderUsage;          // usage category
  gender: 'boy' | 'girl' | 'neutral'; // retained for UI compat

  // ── Content ───────────────────────────────────────────────
  meaning: string | null;
  origin: string | null;             // free-text origin, e.g. "Italian / Latin"
  pronunciation: string | null;      // phonetic guide, e.g. "LOO-kah"
  variants: string[];                // alternate spellings ["Lucia","Lucía","Lucie"]

  // ── Popularity & Trend ────────────────────────────────────
  popularityRank: number | null;     // 1 = most popular (country or worldwide)
  popularityScore: number | null;    // 0–1000 normalized score (higher = more popular)
  trendDirection: TrendDirection;

  // ── Phonetic / Similarity ─────────────────────────────────
  phoneticKey: string | null;        // Soundex key for phonetic grouping

  // ── Tagging ───────────────────────────────────────────────
  tags: string[];                    // merged from style_tags + computed DNA tags

  // ── Region / Locale ───────────────────────────────────────
  region: string;                    // app region key ('EU','US','WORLDWIDE'…)
  is_worldwide: boolean;

  // ── Provenance ────────────────────────────────────────────
  source: NameSource;
  lastUpdated: string;               // ISO timestamp

  // ── Backward-compat aliases (UI layer uses these) ─────────
  name: string;                      // alias → displayName (always in sync)
  style_tags: string[];              // alias → tags
  popularity_score: number | null;   // alias → popularityScore
}

